/**
 * `completions` command (claude-code-profiles-0zn). Emits a static shell
 * completion script to stdout for bash | zsh | fish. The script reads
 * `.claude-profiles/` at tab-time to suggest profile names on `use`/`diff`/
 * `validate`/`sync`. No daemon, no state-file reads — directory listing only.
 *
 * Completions register only the `c3p` binary. The `.claude-profiles/`
 * directory name is the on-disk profile store and stays unchanged.
 *
 * Verbs known to the completer must be kept in sync with VERBS in parse.ts.
 * Adding a verb that takes a profile name? Add it to the verb list AND to the
 * "needs profile names" branch of each shell's completer.
 */

import type { CompletionShell } from "../types.js";
import { EXIT_OK } from "../exit.js";
import type { OutputChannel } from "../output.js";

export interface CompletionsOptions {
  shell: CompletionShell;
  output: OutputChannel;
}

const VERBS = [
  "init",
  "list",
  "use",
  "status",
  "drift",
  "diff",
  "new",
  "validate",
  "sync",
  "hook",
  "doctor",
  "completions",
  "help",
] as const;

const PROFILE_VERBS = ["use", "diff", "validate", "sync"] as const;

const GLOBAL_FLAGS = [
  "--json",
  "--quiet",
  "-q",
  "--cwd",
  "--on-drift",
  "--wait",
  "--no-color",
  "--help",
  "-h",
  "--version",
  "-V",
];

export function runCompletions(opts: CompletionsOptions): number {
  const script = renderScript(opts.shell);
  if (opts.output.jsonMode) {
    // --json: wrap as a structured payload so the channel never produces
    // empty stdout for a successful command (matches `version` / `help`).
    opts.output.json({ shell: opts.shell, script });
  } else {
    opts.output.print(script);
  }
  return EXIT_OK;
}

function renderScript(shell: CompletionShell): string {
  switch (shell) {
    case "bash":
      return BASH;
    case "zsh":
      return ZSH;
    case "fish":
      return FISH;
  }
}

const BASH = `# c3p bash completion (generated by \`c3p completions bash\`)
# Install: source <(c3p completions bash)
#      or: c3p completions bash > /etc/bash_completion.d/c3p
_c3p() {
  local cur prev words cword
  COMPREPLY=()
  cur="\${COMP_WORDS[COMP_CWORD]}"
  prev="\${COMP_WORDS[COMP_CWORD-1]}"

  # Verb position (index 1).
  if [ "\${COMP_CWORD}" -eq 1 ]; then
    COMPREPLY=( $(compgen -W "${VERBS.join(" ")}" -- "\${cur}") )
    return 0
  fi

  local verb="\${COMP_WORDS[1]}"
  case "\${verb}" in
    ${PROFILE_VERBS.join("|")})
      # Suggest profile names from .claude-profiles/<name>/.
      if [ -d ".claude-profiles" ] && [ "\${COMP_CWORD}" -eq 2 ]; then
        local profiles
        profiles=$(find .claude-profiles -mindepth 1 -maxdepth 1 -type d -not -name '.*' 2>/dev/null | sed 's|.claude-profiles/||')
        COMPREPLY=( $(compgen -W "\${profiles}" -- "\${cur}") )
        return 0
      fi
      ;;
    hook)
      if [ "\${COMP_CWORD}" -eq 2 ]; then
        COMPREPLY=( $(compgen -W "install uninstall" -- "\${cur}") )
        return 0
      fi
      ;;
    completions)
      if [ "\${COMP_CWORD}" -eq 2 ]; then
        COMPREPLY=( $(compgen -W "bash zsh fish" -- "\${cur}") )
        return 0
      fi
      ;;
  esac

  # Default: complete global flags.
  COMPREPLY=( $(compgen -W "${GLOBAL_FLAGS.join(" ")}" -- "\${cur}") )
}
complete -F _c3p c3p
`;

const ZSH = `#compdef c3p
# c3p zsh completion (generated by \`c3p completions zsh\`)
# Install: c3p completions zsh > "\${fpath[1]}/_c3p"
#      or: source <(c3p completions zsh)
_c3p() {
  local context state line
  typeset -A opt_args

  _arguments -C \\
    '1: :->verb' \\
    '*::arg:->args'

  case $state in
    verb)
      local -a verbs
      verbs=(${VERBS.map((v) => `'${v}'`).join(" ")})
      _describe 'command' verbs
      ;;
    args)
      case $line[1] in
        ${PROFILE_VERBS.join("|")})
          if [ -d .claude-profiles ]; then
            local -a profiles
            profiles=(\${(f)"$(find .claude-profiles -mindepth 1 -maxdepth 1 -type d -not -name '.*' 2>/dev/null | sed 's|.claude-profiles/||')"})
            _describe 'profile' profiles
          fi
          ;;
        hook)
          local -a actions
          actions=('install' 'uninstall')
          _describe 'action' actions
          ;;
        completions)
          local -a shells
          shells=('bash' 'zsh' 'fish')
          _describe 'shell' shells
          ;;
        *)
          _arguments ${GLOBAL_FLAGS.map((f) => `'${f}'`).join(" ")}
          ;;
      esac
      ;;
  esac
}
compdef _c3p c3p
`;

const FISH = `# c3p fish completion (generated by \`c3p completions fish\`)
# Install: c3p completions fish > ~/.config/fish/completions/c3p.fish

function __c3p_profiles
  if test -d .claude-profiles
    find .claude-profiles -mindepth 1 -maxdepth 1 -type d -not -name '.*' 2>/dev/null | sed 's|.claude-profiles/||'
  end
end

# Verbs (only suggested when no verb has been given yet).
${VERBS.map((v) => `complete -c c3p -n '__fish_use_subcommand' -a '${v}'`).join("\n")}

# Profile-name completion for verbs that take one.
${PROFILE_VERBS.map((v) => `complete -c c3p -n '__fish_seen_subcommand_from ${v}' -a '(__c3p_profiles)'`).join("\n")}

# hook actions.
complete -c c3p -n '__fish_seen_subcommand_from hook' -a 'install uninstall'

# completions shells.
complete -c c3p -n '__fish_seen_subcommand_from completions' -a 'bash zsh fish'

# Global flags.
${GLOBAL_FLAGS.filter((f) => f.startsWith("--"))
  .map((f) => `complete -c c3p -l ${f.slice(2)}`)
  .join("\n")}
`;
